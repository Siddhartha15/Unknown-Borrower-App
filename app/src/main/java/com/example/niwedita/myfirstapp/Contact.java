package com.example.niwedita.myfirstapp;


public class Contact {
    String name;
    String image;
    String city;
    String organization;
    String num;
    int userId;

    public Contact() {
    }

    public String getName() {

        return name;
    }

    public String getImage() {

        return image;
    }

    public String getCity() {

        return city;
    }
    public String getOrganization() {

        return organization;
    }
    public String getContact() {

        return num;
    }
    public int getUserId() {

        return userId;
    }
}

